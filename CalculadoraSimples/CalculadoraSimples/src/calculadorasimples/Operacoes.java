
package calculadorasimples;

public class Operacoes {
    public static double somaValor(double num1, double num2){
        return (num1+num2);
    }
    
    public static double subtValor(double num1, double num2){
        return (num1-num2);
    }
    
    public static double multValor(double num1, double num2){
        return (num1*num2);
    }
    
    public static double divValor(double num1, double num2){
        return (num1/num2);
    }
}
